    def set_source(self, source_user_id):
        """Sets a new source user and runs a full SSSP-Del from scratch."""
        if source_user_id not in self.all_users:
            print(f"Error: User ID {source_user_id} not found in graph.")
            return 0.0 # Return 0 time for error

        print(f"\n--- Setting new source to {source_user_id} and running initial SSSP-Del ---")
        self.source_user_id = source_user_id

        # Reset SSSP state for all users
        for user in self.all_users.values():
            user.reset_sssp()

        # Set source distance and propagate
        source = self.all_users.get(self.source_user_id)
        if source: # Check if source exists
             source.distance = 0
        else:
             print(f"Error: Source user {source_user_id} somehow not found after check.")
             return 0.0 # Indicate error

        self.message_queue.clear()
        # Propagate from source only if it exists
        if source:
            for friend_id, weight in source.out_edges.items():
                 # Check if friend_id exists before sending message
                 if friend_id in self.all_users:
                     self.message_queue.append(Message(source.id, friend_id, MessageType.DISTANCE_UPDATE, {'distance': weight}))

        start_time = time.perf_counter()
        self.process_queue()
        end_time = time.perf_counter()

        print("--- Initial SSSP-Del complete. Shortest path tree is built. ---")
        return end_time - start_time

    def process_queue(self):
        """Simulates processing all SSSP-Del messages until convergence."""
        #print(f"  --- Processing {len(self.message_queue)} messages ---")
        i = 0
        processed_count = 0
        # More robust failsafe, check queue length too
        max_messages = len(self.all_users) * len(self.all_users) * 5
        while self.message_queue:
            if i > max_messages:
                 print(f"  --- Hit processing limit of {i} messages, stopping. Queue size: {len(self.message_queue)} ---")
                 # Print some messages from the queue for debugging
                 for _ in range(min(5, len(self.message_queue))):
                     print(f"      Sample pending msg: {self.message_queue[_]}")
                 break
            i += 1
            msg = self.message_queue.popleft()
            receiver = self.all_users.get(msg.to_id)
            if receiver:
                receiver.on_message(msg, self.message_queue, self.all_users)
                processed_count += 1
        #print(f"  --- Message queue empty after {processed_count} messages processed. System converged. ---")

