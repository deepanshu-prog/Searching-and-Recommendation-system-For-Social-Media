    def _break_single_edge(self, u_id, v_id):
        """Internal helper to break a single-direction edge and trigger SSSP-Del."""
        u = self.all_users.get(u_id)
        v = self.all_users.get(v_id)

        if not u or not v or v_id not in u.out_edges:
            print(f"    (Edge {u_id}->{v_id} does not exist or already broken.)")
            return

        # SSSP-Del logic only triggers if a benchmark source is set
        if self.source_user_id:
            # Epoch check needs to happen before state change
             if self.message_queue:
                print("Warning: Message queue not empty before deletion epoch. Processing first.")
                self.process_queue() # Ensure convergence before proceeding
             assert not self.message_queue, "Epoch failed: Queue must be empty before deletion."

        print(f"    Breaking directed edge: {u_id}->{v_id}")
        old_weight = u.out_edges.get(v_id, math.inf) # Get weight before deleting

        # --- MODIFIED: More robust deletion ---
        if v_id in u.out_edges:
            del u.out_edges[v_id]
            print(f"      Removed {v_id} from out_edges of {u_id}. Remaining: {list(u.out_edges.keys())}")
        if u_id in v.in_edges:
            del v.in_edges[u_id]
            print(f"      Removed {u_id} from in_edges of {v_id}. Remaining: {list(v.in_edges.keys())}")

        # SSSP-Del logic only triggers if a benchmark source is set
        if self.source_user_id:
            v_exists = v_id in self.all_users
            u_is_reachable = (u.distance != math.inf)

            # --- MODIFIED: Use math.isclose for checking critical path ---
            # Check if the path was critical *using the old weight*
            # Check if predecessor is u_id before checking distance condition
            is_critical_path = v_exists and v.predecessor == u_id and u_is_reachable
            if is_critical_path:
                 # Now check the distance condition more carefully
                 # Ensure old_weight is valid before comparison
                 if old_weight != math.inf:
                      # Check if u actually exists before accessing its distance
                      if u:
                           is_critical_path = math.isclose(v.distance, u.distance + old_weight, rel_tol=FLOAT_TOLERANCE)
                      else: # Should not happen if u_is_reachable is true
                           is_critical_path = False
                 else:
                      is_critical_path = False # Cannot be critical if weight was inf


            if is_critical_path:
                print(f"    SSSP-Del Path for V({v_id}) was broken! (Old cost: {v.distance:.4f})") # Increased precision

                # --- PHASE 1: Invalidation ---
                print("\n  --- Starting Phase 1: Invalidation ---")
                self.message_queue.append(Message(u_id, v_id, MessageType.SET_TO_INFINITY, {}))
                self.process_queue()

                # --- PHASE 2: Recomputation ---
                print("\n  --- Starting Phase 2: Recomputation ---")
                affected_vertices = []
                for user in self.all_users.values():
                    if user.marked_as_infinity:
                        user.marked_as_infinity = False
                        affected_vertices.append(user)
                print(f"    {len(affected_vertices)} vertices were affected. Querying neighbors...")
                for user in affected_vertices:
                    # Query all *remaining* incoming neighbors
                    for neighbor_id in user.in_edges.keys():
                         # Ensure neighbor exists before querying
                         if neighbor_id in self.all_users:
                             self.message_queue.append(Message(user.id, neighbor_id, MessageType.DISTANCE_QUERY, {}))

                self.process_queue()
            else:
                 # Check why it wasn't critical
                 reason = "unknown" # Default reason
                 if not v_exists: reason = "v does not exist"
                 elif v.predecessor != u_id: reason = f"v's predecessor is {v.predecessor}, not {u_id}"
                 elif not u_is_reachable: reason = f"u ({u_id}) was not reachable"
                 elif u is None: reason = f"u ({u_id}) node object is missing" # Added check
                 elif old_weight == math.inf: reason = "old weight was infinity"
                 # Check distance condition carefully only if u exists
                 elif u and not math.isclose(v.distance, u.distance + old_weight, rel_tol=FLOAT_TOLERANCE):
                      reason = f"distance condition false ({v.distance:.4f} vs {u.distance + old_weight:.4f})"

                 print(f"    Edge {u_id}->{v_id} was deleted, but it was not the SSSP-Del tree's shortest path for V({v_id}). Reason: {reason}. No SSSP-Del changes needed.")

    def simulate_batch_update(self, batch_df):
        """
        --- NOVELTY: Efficient Batched Updates ---
        Processes a batch of additions and deletions together.
        """
        print(f"\n>>> DYNAMIC BATCH CHANGE: Processing {len(batch_df)} events ---")
        if not self.source_user_id:
            print("Error: Please set a source user first with 'source <id>'.")
            return 0.0

        start_time = time.perf_counter()

        # --- 1. Apply all structural changes and collect initial messages ---

        # Ensure queue is empty before starting batch
        if self.message_queue:
            print("Warning: Processing non-empty queue before batch.")
            self.process_queue()
        assert not self.message_queue, "Epoch failed: Queue must be empty before batch."

        initial_add_messages = []
        initial_delete_messages = []
        affected_nodes_from_deletes = set()

        for _, row in batch_df.iterrows():
            op = row.get('operation')
            u_id = int(row.get('u_id'))
            v_id = int(row.get('v_id'))

            u = self.all_users.get(u_id)
            v = self.all_users.get(v_id)
            if not u or not v:
                print(f"    Skipping batch event: User {u_id} or {v_id} not found.")
                continue

            if op == 'add':
                weight = float(row.get('weight'))
                if not (FLOAT_TOLERANCE <= weight <= 1.0):
                     print(f"    Skipping batch add: Invalid weight {weight}.")
                     continue

                print(f"    Batch ADD: {u_id} -> {v_id} (weight={weight})")
                u.out_edges[v_id] = weight
                v.in_edges[u_id] = weight

                # Check if this new edge offers a shortcut
                if u.distance != math.inf and (u.distance + weight) < (v.distance - FLOAT_TOLERANCE):
                    initial_add_messages.append(Message(u_id, v_id, MessageType.DISTANCE_UPDATE, {'distance': u.distance + weight}))

            elif op == 'delete':
                print(f"    Batch DELETE: {u_id} -> {v_id}")

                old_weight = u.out_edges.get(v_id, math.inf)

                if v_id in u.out_edges: del u.out_edges[v_id]
                if u_id in v.in_edges: del v.in_edges[u_id]

                # Check if this was a critical path
                u_is_reachable = (u.distance != math.inf)
                is_critical_path = (v.predecessor == u_id) and u_is_reachable
                if is_critical_path:
                    if old_weight != math.inf:
                        is_critical_path = math.isclose(v.distance, u.distance + old_weight, rel_tol=FLOAT_TOLERANCE)
                    else:
                        is_critical_path = False

                if is_critical_path:
                    print(f"      Path for V({v_id}) was broken! (Old cost: {v.distance:.4f})")
                    # Collect nodes to invalidate. Use tuple to avoid duplicates if same node
                    # is invalidated by multiple deletions.
                    initial_delete_messages.append(Message(u_id, v_id, MessageType.SET_TO_INFINITY, {}))
                    affected_nodes_from_deletes.add(v_id)


        # --- 2. Run ONE combined Invalidation + Addition pass ---
        print(f"\n  --- Batch Phase 1: Invalidation + Addition Pass ---")
        # Add all messages to the queue
        for msg in initial_delete_messages: self.message_queue.append(msg)
        for msg in initial_add_messages: self.message_queue.append(msg)

        self.process_queue() # This will propagate all invalidations and new paths

        # --- 3. Run ONE combined Recomputation pass ---
        print("\n  --- Batch Phase 2: Recomputation Pass ---")
        affected_vertices = []
        for user in self.all_users.values():
            if user.marked_as_infinity:
                user.marked_as_infinity = False
                affected_vertices.append(user)

        # Also include nodes that were *directly* hit by a delete,
        # even if they weren't marked (e.g., if path was already inf)
        for v_id in affected_nodes_from_deletes:
             user = self.all_users.get(v_id)
             if user and user not in affected_vertices:
                  affected_vertices.append(user) # Will query even if not marked

        print(f"    {len(affected_vertices)} unique vertices affected by batch. Querying neighbors...")

        # Enqueue all query messages *first*
        for user in affected_vertices:
            for neighbor_id in user.in_edges.keys():
                 if neighbor_id in self.all_users:
                     self.message_queue.append(Message(user.id, neighbor_id, MessageType.DISTANCE_QUERY, {}))

        # Now, process all queries and subsequent updates until convergence
        self.process_queue()

        end_time = time.perf_counter()
        print("--- Batch update complete. ---")
        return end_time - start_time


    def get_sssp_path_list(self, destination_id):
        """
        Traces the SSSP-Del tree to find the shortest path list from the *active source*.
        Returns a list of user IDs in the path, or None if unreachable/error.
        """
        if not self.source_user_id:
            print("Error: No SSSP source set for get_sssp_path_list.")
            return None
        if destination_id not in self.all_users:
             print(f"Error: Destination {destination_id} not in graph.")
             return None

        user = self.all_users.get(destination_id)
        if not user or user.distance == math.inf: # Add check for user existence
            return None

        path_list = []
        curr = user
        path_len = 0
        # Increased loop protection
        max_path_len = len(self.all_users) + 1
        visited_in_trace = set() # Detect cycles during trace

        while curr is not None and path_len < max_path_len:
            if curr.id in visited_in_trace:
                 print(f"Warning: Cycle detected at {curr.id} during path reconstruction.")
                 return None # Cycle detected
            visited_in_trace.add(curr.id)

            path_list.append(curr.id)
            if curr.id == self.source_user_id:
                break # Found source

            if curr.predecessor is None:
                 # This indicates a potential issue if distance is not inf
                 print(f"Warning: Path reconstruction stopped at {curr.id} with no predecessor, but distance is {curr.distance:.4f}")
                 return None

            if curr.predecessor not in self.all_users:
                 print(f"Warning: Predecessor {curr.predecessor} for user {curr.id} not found in all_users.")
                 return None # Error in path data

            # Check for immediate self-loop cycle
            if curr.predecessor == curr.id:
                 print(f"Warning: Self-loop cycle detected at {curr.id} during path reconstruction.")
                 return None

            curr = self.all_users.get(curr.predecessor) # Use .get() for safety
            path_len += 1

        if path_len >= max_path_len:
             print(f"Warning: Path reconstruction exceeded max length ({max_path_len}) for dest {destination_id}. Potential cycle.")
             return None # Cycle detected or error

        path_list.reverse()

        # Final validation
        if not path_list or path_list[0] != self.source_user_id:
             # print(f"Warning: Path construction failed for dest {destination_id}. Result: {path_list}")
             return None # Error in path construction

        return path_list


