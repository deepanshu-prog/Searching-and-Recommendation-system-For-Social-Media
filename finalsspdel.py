def run_static_dijkstra_benchmark(all_users, source_id):
    """
    Runs a full, static Dijkstra algorithm from scratch for benchmarking.
    This is the "naive recompute" baseline. Returns *only* the time.
    """
    if source_id not in all_users:
        return 0.0

    start_time = time.perf_counter()

    distances = {user_id: math.inf for user_id in all_users}
    distances[source_id] = 0
    pq = [(0, source_id)] # (distance, user_id)

    processed_nodes = set() # Avoid reprocessing

    while pq:
        current_distance, u_id = heapq.heappop(pq)

        # Use tolerance and check if already processed
        if u_id in processed_nodes or current_distance > distances[u_id] + FLOAT_TOLERANCE:
            continue
        processed_nodes.add(u_id)

        u = all_users.get(u_id)
        if not u: continue

        for v_id, weight in u.out_edges.items():
            if v_id in distances:
                distance = current_distance + weight
                # Use tolerance
                if distance < distances[v_id] - FLOAT_TOLERANCE:
                    distances[v_id] = distance
                    # Add to heap even if already processed, Dijkstra handles cycles
                    heapq.heappush(pq, (distance, v_id))

    end_time = time.perf_counter()
    return end_time - start_time

def run_static_p2p_path(all_users, source_id, dest_id):
    """
    Runs a static Dijkstra to find a single path from source to destination.
    Returns (time, path, distance).
    """
    if source_id not in all_users or dest_id not in all_users:
        return 0.0, None, math.inf

    start_time = time.perf_counter()

    distances = {user_id: math.inf for user_id in all_users}
    predecessors = {user_id: None for user_id in all_users}
    distances[source_id] = 0

    pq = [(0, source_id)] # (distance, user_id)

    processed_nodes = set() # Add this to prevent reprocessing with stale distances

    path_found = False # Flag to indicate if destination was reached

    while pq:
        current_distance, u_id = heapq.heappop(pq)

        # Optimization: if distance is worse than already found, skip
        if current_distance > distances[u_id] + FLOAT_TOLERANCE:
            continue

        # Optimization: if already processed, skip (important for graphs with cycles)
        if u_id in processed_nodes:
             continue
        processed_nodes.add(u_id)


        # If we've found the destination, we can stop early
        if u_id == dest_id:
            path_found = True
            break

        u = all_users.get(u_id)
        if not u: continue

        for v_id, weight in u.out_edges.items():
            if v_id in distances:
                distance = current_distance + weight
                # Use tolerance
                if distance < (distances[v_id] - FLOAT_TOLERANCE):
                    distances[v_id] = distance
                    predecessors[v_id] = u_id
                    heapq.heappush(pq, (distance, v_id))

    end_time = time.perf_counter()
    d_time = end_time - start_time

    # Reconstruct path only if destination was reached
    path = []
    if path_found and distances[dest_id] != math.inf:
        curr = dest_id
        path_len = 0
        max_path_len = len(all_users) + 1
        visited_in_trace = set() # Cycle detection for reconstruction

        while curr is not None and path_len < max_path_len:
            if curr in visited_in_trace:
                 print(f"Warning: Cycle detected at {curr} during Dijkstra path reconstruction.")
                 return d_time, None, math.inf
            visited_in_trace.add(curr)

            path.append(str(curr))
            if curr == source_id:
                 break # Reached source

            pred = predecessors.get(curr)
            if pred is None:
                 if curr != source_id:
                     print(f"Warning: Dijkstra path reconstruction broke at {curr}. Predecessor map entry missing or None.")
                     return d_time, None, math.inf # Error in path
                 else:
                     break # Reached source correctly

            curr = pred
            path_len += 1

        if path_len >= max_path_len:
            print(f"Warning: Dijkstra path reconstruction exceeded max length ({max_path_len}) for dest {dest_id}. Potential cycle.")
            return d_time, None, math.inf # Cycle or error

        path.reverse()

        # Final validation
        if not path or int(path[0]) != source_id:
            #print(f"Warning: Dijkstra path construction failed for dest {dest_id}. Result: {path}")
            return d_time, None, math.inf # Path not found

        return d_time, path, distances[dest_id]
    else:
        # Destination was not reached or distance is still infinity
        return d_time, None, math.inf


